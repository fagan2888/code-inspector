func TestEndsSetto(t *testing.T) {
	var z stemmer
	z.b = []byte("blowjobs")
	z.k = len(z.b) - 1
	k := z.k

	b := z.ends([]byte("jobs"))
	if !b {
		t.Errorf("Doesn't work at all")
	}
	//blowjobs
	//01234
	if z.j != 3 {
		t.Errorf("j not properly incremented: %d", z.j)
	}
	if z.k != k {
		t.Errorf("k should not have been incremented: %d", z.k)
	}

	j := z.j
	nsuf := []byte("n")
	z.setto(nsuf)
	if z.j != j {
		t.Errorf("j should not have been incremented")
	}

	if z.k != j+len(nsuf) {
		fmt.Printf("b : %x (%s)\n", z.b, string(z.b))
		t.Errorf("k incorrect, have: %d want: %d", z.k, j+1+len(nsuf))
	}

	var test = []string{
		"ponies",
		"ties",
	}

	for _, term := range test {
		z := stemmer{[]byte(term), 0, len(term) - 1}
		if z.ends(_IES) {
			z.setto(_I)
			if !z.ends(_I) {
				t.Errorf("setto failed, %s(%d) doesn't ends(%s)\n", string(z.b), z.k, _I)
			}
		} else {
			t.Errorf("ends/setto failed, %s doesn't ends(%s)\n", term, _IES)
		}
	}

}

